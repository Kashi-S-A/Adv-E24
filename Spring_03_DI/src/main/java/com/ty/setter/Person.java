package com.ty.setter;

public class Person {

	private String name;

	private long phone;

	public String getName() {
		return name;
	}

	public long getPhone() {
		return phone;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setPhone(long phone) {
		this.phone = phone;
	}

}
